# Índice por nome para busca O(1)
_INDICE_NOME: dict[str, dict] = {m["nome"].lower(): m for m in MUNICIPIOS_SP}

# Regiões disponíveis
REGIOES_SP: list[str] = sorted({m["regiao"] for m in MUNICIPIOS_SP})


def buscar_municipio(nome: str) -> dict | None:
    """
    Busca município por nome exato (case-insensitive).
    Retorna dict com nome, lat, lon, regiao ou None se não encontrado.
    """
    return _INDICE_NOME.get(nome.strip().lower())


def listar_municipios(regiao: str = "") -> list[str]:
    """
    Retorna lista de nomes dos municípios, opcionalmente filtrada por região.
    Ordenada alfabeticamente.
    """
    if regiao:
        return sorted(m["nome"] for m in MUNICIPIOS_SP if m["regiao"] == regiao)
    return sorted(m["nome"] for m in MUNICIPIOS_SP)


def buscar_municipios_por_prefixo(prefixo: str, limite: int = 10) -> list[str]:
    """
    Retorna até `limite` municípios cujo nome começa com `prefixo`.
    Útil para autocomplete em tempo real.
    """
    pref = prefixo.strip().lower()
    return [
        m["nome"] for m in MUNICIPIOS_SP
        if m["nome"].lower().startswith(pref)
    ][:limite]